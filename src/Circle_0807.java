public class Circle_0807
{
    private double radius;

    public Circle_0807()
    {
        this.radius = 1;
    }

    public void setRadius(int radius)
    {
        this.radius = radius;
    }

    public double getRadius()
    {
        return radius;
    }

    public double findArea()
    {
        return 3.14*radius*radius;
    }


}
