class VaribleTest
{
	public static void main(String[] args)
	{
		byte a = 127;
		short b = 32767;
		int c = 2100000000;
		long d = 9999999999L;
		float e = 3.445547F;
		double f = 2.76e8;
		
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		System.out.println("d="+d);
		System.out.println("e="+e);
		System.out.println("f="+f);
	}




}